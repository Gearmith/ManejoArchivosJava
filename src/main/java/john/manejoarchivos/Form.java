        private void buttonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAnadirActionPerformed
		String nombre = anadirNombreField.getText();
		String numero = numeroField.getText();
		
		if (nombre.equals("")) {
			JOptionPane.showMessageDialog(null, "Se debe ingresar un nombre");
		} else {
			Crud crud = new Crud();
			crud.crearRegistro(nombre, numero);
		}
        }//GEN-LAST:event_buttonAnadirActionPerformed

